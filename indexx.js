function loadWords() {
    const wordTableBody = document.getElementById('wordTable').getElementsByTagName('tbody')[0];
    wordTableBody.innerHTML = ''; // 清空现有列表

    words.forEach(wordData => {
        const row = wordTableBody.insertRow();
        row.insertCell(0).textContent = wordData.word;
        row.insertCell(1).innerHTML = `<span class="pinyin">${wordData.pinyin}</span>`;
        row.insertCell(2).textContent = wordData.meanings.map(meaning => meaning.meaning).join('；');
        const examplesCell = row.insertCell(3);
        const examplesList = document.createElement('div');
        wordData.meanings.forEach(meaningData => {
            meaningData.examples.forEach(example => {
                const exampleDiv = document.createElement('div');
                exampleDiv.textContent = example;
                const audioButton = document.createElement('button');
                audioButton.textContent = '听例句';
                audioButton.onclick = () => playAudio(meaningData.exampleAudioSrc);
                exampleDiv.appendChild(audioButton);
                examplesList.appendChild(exampleDiv);
            });
        });
        examplesCell.appendChild(examplesList);
        const pinyinButton = document.createElement('button');
        pinyinButton.textContent = '听单词';
        pinyinButton.onclick = () => playAudio(wordData.audioSrc);
        row.insertCell(4).appendChild(pinyinButton);
    });
}

function playAudio(src) {
    const audio = new Audio(src);
    audio.play();
}

function searchWord() {
    const input = document.getElementById('searchInput');
    const filter = input.value.toLowerCase();
    const wordTableBody = document.getElementById('wordTable').getElementsByTagName('tbody')[0];
    const rows = wordTableBody.getElementsByTagName('tr');

    for (let i = 0; i < rows.length; i++) {
        const wordCell = rows[i].getElementsByTagName('td')[0];
        if (wordCell) {
            const wordText = wordCell.textContent.toLowerCase();
            if (wordText.indexOf(filter) > -1) {
                rows[i].style.display = "";
            } else {
                rows[i].style.display = "none";
            }
        }
    }
}

// 加载单词列表
loadWords();

document.getElementById('searchInput').addEventListener('keyup', () => {
    if (event.key === 'Enter') {
        searchWord();
    }
});
